using Content.Client.Computer;
using Content.Client.UserInterface.Controls;
using Content.Shared._Adventure.Artillery;
using JetBrains.Annotations;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;
using Robust.Shared.Network;
using System.Linq;
using System.Numerics;

namespace Content.Client._Adventure.Artillery;

[GenerateTypedNameReferences]
public sealed partial class ArtilleryConsoleWindow : BaseWindow, IComputerWindow<ArtilleryConsoleBoundUserInterfaceState>
{
    private ComputerBoundUserInterfaceBase? _computerBoundUserInterface;
    private NetEntity _selectedBeacon = NetEntity.Invalid;

    public ArtilleryConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        CloseButton.OnPressed += _ => Close();
    }

    protected override DragMode GetDragModeFor(Vector2 relativeMousePos)
    {
        return DragMode.Move;
    }

    public void SetupComputerWindow(ComputerBoundUserInterfaceBase cb)
    {
        _computerBoundUserInterface = cb;
        FireButton.OnPressed += _ => cb.SendMessage(new ArtilleryConsoleFireMessage());
        ToggleButton.OnToggled += args => cb.SendMessage(new ArtilleryConsoleToggleMessage());
    }

    public void UpdateState(ArtilleryConsoleBoundUserInterfaceState state)
    {
        BeaconList.RemoveAllChildren();
        _selectedBeacon = state.SelectedBeacon;

        var hasBeacons = state.Beacons.Count > 0;
        NoBeaconsText.Visible = !hasBeacons;
        BeaconList.Visible = hasBeacons;

        foreach (var beacon in state.Beacons)
        {
            var button = new MonotoneButton
            {
                Text = beacon.Name,
                ToggleMode = true,
                Pressed = beacon.NetEntity == state.SelectedBeacon,
                StyleClasses = { "OpenBoth" },
                HorizontalExpand = true,
                Modulate = beacon.NetEntity == state.SelectedBeacon ? Color.FromHex("#00FFFF") : Color.White
            };

            button.OnToggled += args =>
            {
                _selectedBeacon = beacon.NetEntity;
                _computerBoundUserInterface?.SendMessage(new ArtilleryConsoleSelectBeaconMessage(beacon.NetEntity));

                foreach (var child in BeaconList.Children.OfType<MonotoneButton>())
                {
                    child.Pressed = false;
                    child.Modulate = Color.White;
                }
                button.Pressed = true;
                button.Modulate = Color.FromHex("#00FFFF");
            };

            BeaconList.AddChild(button);
        }

        FireButton.Disabled = !state.CanFire;
        ToggleButton.Disabled = !state.CanToggle;
        ToggleButton.Pressed = state.CannonEnabled;
        ToggleButton.Text = state.CannonEnabled
            ? Loc.GetString("artillery-console-toggle-on")
            : Loc.GetString("artillery-console-toggle-off");

        var powerText = $"Статус: {state.ReceivedPower:F0}W / {state.RequiredPower:F0}W";
        if (!state.HasCannon)
            powerText = "Орудие не обнаружено";

        CooldownLabel.Text = state.FullyPowered ? GetStatusText(state) : powerText;
        Footer.Text = Loc.GetString("artillery-console-warning");
    }

    private static string GetStatusText(ArtilleryConsoleBoundUserInterfaceState state)
    {
        if (!state.HasCannon)
            return Loc.GetString("artillery-console-no-cannon");

        if (state.Beacons.Count == 0)
            return Loc.GetString("artillery-console-no-beacons");

        if (state.CooldownRemaining > TimeSpan.Zero)
            return Loc.GetString("artillery-console-cooldown",
                ("time", state.CooldownRemaining.ToString(@"mm\:ss")));

        return Loc.GetString("artillery-console-ready");
    }
}

[UsedImplicitly]
public sealed class ArtilleryConsoleBoundUserInterface
    : ComputerBoundUserInterface<ArtilleryConsoleWindow, ArtilleryConsoleBoundUserInterfaceState>
{
    public ArtilleryConsoleBoundUserInterface(EntityUid owner, Enum uiKey) : base(owner, uiKey)
    {
    }
}
